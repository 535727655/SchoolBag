package Entity;

public class ReplyEntity {
	private int repId;
	private int repforid;
	private String repUserid;
	private String repContent;
	public int getRepId() {
		return repId;
	}
	public void setRepId(int repId) {
		this.repId = repId;
	}
	public int getRepforid() {
		return repforid;
	}
	public void setRepforid(int repforid) {
		this.repforid = repforid;
	}
	public String getRepUserid() {
		return repUserid;
	}
	public void setRepUserid(String repUserid) {
		this.repUserid = repUserid;
	}
	public String getRepContent() {
		return repContent;
	}
	public void setRepContent(String repContent) {
		this.repContent = repContent;
	}
	
}
